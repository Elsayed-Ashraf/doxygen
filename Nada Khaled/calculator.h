/** @file calculator.h
 *  @brief Function prototypes for the console driver.
 *
 *  This contains the prototypes for the simple calculator project
 *
 *  @author Nada Khaled
 *  @bug No known bugs.
 */


/** @brief adds two given numbers
 *
 * @param x is the first number
 * @param y is the second number
 * @return the sum
 *
 */

int Add (int x, int y);

/** @brief subtracts two given numbers
 *
 * @param a is the first number
 * @param b is the second number
 * @return the difference
 *
 */
int Sub(int a,int b);

/** @brief multiplies two given numbers
 *
 * @param x is the first number
 * @param y is the second number
 * @return the product
 *
 */
int Multiply (int x,int y);


/** @brief divides two given numbers
 *
 * @param i is the first number
 * @param j is the second number
 * @return the quotient
 *
 */
float divide (int i,int j);

